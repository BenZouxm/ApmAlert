
 DROP TABLE IF EXISTS `ALERT_RECORD`;
 CREATE TABLE `ALERT_RECORD`  (
  `ID` bigint(20) NOT NULL AUTO_INCREMENT,

  `ALERTID` VARCHAR(500) DEFAULT NULL,
  `RECORD` VARCHAR(1000) DEFAULT NULL,
  `RECVTIME` DATETIME DEFAULT NULL,
  `SENDTIME` DATETIME DEFAULT NULL,
  `ISSEND` BIT DEFAULT NULL,
  `SENDREP` VARCHAR(500) DEFAULT NULL,
  `ALERTSOURECODE` INT DEFAULT NULL,
  `RESENDTIMES` INT NOT NULL  DEFAULT 0 ,
